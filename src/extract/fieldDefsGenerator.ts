/**
 * Field Definitions Generator
 *
 * Turns extracted workbook fields into `fields.d.twbl` declaration blocks that
 * FieldParser can consume (JSDoc description + `[Name] = Type`), giving the
 * LSP hover/completion/lint awareness of the workbook's real fields.
 *
 * The output file is organized into per-datasource sections delimited by
 * marker comments, so regenerating one datasource replaces only its own
 * section and leaves the others untouched.
 */

import { ExtractedField } from './types.js';

/** Tableau datatype -> declaration type used by fields.d.twbl / twbl.d.twbl */
const DATATYPE_MAP: Record<string, string> = {
    integer: 'Number',
    real: 'Number',
    string: 'String',
    boolean: 'Boolean',
    date: 'Date',
    datetime: 'DateTime',
    spatial: 'Spatial'
};

export function mapDatatype(datatype: string | undefined): string {
    return DATATYPE_MAP[(datatype ?? '').toLowerCase()] ?? 'String';
}

function sectionStartMarker(datasource: string, workbook: string): string {
    return `// === Workbook: ${workbook} | Datasource: ${datasource} ===`;
}

function sectionEndMarker(datasource: string, workbook: string): string {
    return `// === End Workbook: ${workbook} | Datasource: ${datasource} ===`;
}

/**
 * Generates one datasource's declaration section.
 * Fields are deduplicated by display name (first occurrence wins); fields
 * whose names contain `]` are skipped — they cannot be expressed in the
 * `[Name] = Type` syntax FieldParser reads.
 */
export function generateFieldDefsSection(
    fields: ExtractedField[],
    datasource: string,
    workbook: string
): string {
    const lines: string[] = [sectionStartMarker(datasource, workbook)];
    const seen = new Set<string>();

    for (const field of fields) {
        const name = (field.caption ?? field.name ?? '').trim();
        if (!name || name.includes(']')) {
            continue;
        }
        const key = name.toUpperCase();
        if (seen.has(key)) {
            continue;
        }
        seen.add(key);

        const role = field.role ?? 'unknown';
        const datatype = field.datatype ?? 'unknown';
        lines.push('');
        lines.push('/**');
        lines.push(` * ${name} — ${role}, ${datatype} · ${datasource} (${workbook})`);
        lines.push(' */');
        lines.push(`[${name}] = ${mapDatatype(field.datatype)}`);
    }

    lines.push('');
    lines.push(sectionEndMarker(datasource, workbook));
    return lines.join('\n');
}

/**
 * Inserts or replaces a datasource's section inside existing file content.
 * Returns the full new file content.
 */
export function upsertDatasourceSection(
    existingContent: string,
    datasource: string,
    section: string
): string {
    const header =
        '/**\n' +
        ' * Workbook field definitions generated by Tableau Language Support.\n' +
        ' * One section per datasource — regenerate a datasource from the\n' +
        ' * Tableau Tools sidebar to refresh its section.\n' +
        ' */\n';

    const sectionLines = section.split(/\r?\n/);
    const start = sectionLines[0];
    const end = sectionLines[sectionLines.length - 1];

    const content = existingContent.trim();
    if (!content) {
        return `${header}\n${section}\n`;
    }

    let startIdx = content.indexOf(start);
    let endIdx = content.indexOf(end);
    // Upgrade a pre-1.6 legacy datasource-only section in place when possible.
    if (startIdx === -1 || endIdx === -1) {
        const legacyStart = `// === Datasource: ${datasource} ===`;
        const legacyEnd = `// === End: ${datasource} ===`;
        startIdx = content.indexOf(legacyStart);
        endIdx = content.indexOf(legacyEnd);
        if (startIdx !== -1 && endIdx !== -1) {
            const before = content.slice(0, startIdx).trimEnd();
            const after = content.slice(endIdx + legacyEnd.length).trimStart();
            return [before, section, after].filter(Boolean).join('\n\n') + '\n';
        }
    }
    if (startIdx !== -1 && endIdx !== -1 && endIdx > startIdx) {
        const before = content.slice(0, startIdx).trimEnd();
        const after = content.slice(endIdx + end.length).trimStart();
        const parts = [before, section, after].filter(p => p !== '');
        return parts.join('\n\n') + '\n';
    }

    return `${content}\n\n${section}\n`;
}
